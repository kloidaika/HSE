def distance(x1, y1, x2, y2):
    x = max(x1, x2) - min(x1, x2)
    y = max(y1, y2) - min(y1, y2)
    return (x**2 + y**2)**(0.5)
x1 = int(input())
y1 = int(input())
x2 = int(input())
y2 = int(input())
x3 = int(input())
y3 = int(input())
d1 = distance(x1, y1, x2, y2)
d2 = distance(x1, y1, x3, y3)
d3 = distance(x2, y2, x3, y3)
print('{0:.10f}'.format(d1 + d2 + d3))
